package com.aasctc.template_rules;

public class Namespace {
	private String path;
	private String alias;
	
	public Namespace() {}
	
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}

	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
	
}
